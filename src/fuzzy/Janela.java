    //Parametros do sistema Fuzzy
    String fileName = "regras.fcl";
    FIS fis = FIS.load(fileName);
    JDialogFis dialog = new JDialogFis(fis);

    public Janela() {
        initComponents();
        velocidade = sliderVelocidade.getValue();
        lblVelocidade.setText(String.valueOf(distancia) + " km/h");
        distancia = sliderDistancia.getValue();
        lblDistancia.setText(String.valueOf(distancia) + " m");
        lblResultado.setText("");
        
    }
    
    void fuzzifica(){
        fis.setVariable("Velocidade", velocidade);
        fis.setVariable("Distancia", distancia);
        fis.evaluate();
        Variable resp = fis.getVariable("pressao_freio");
        Double saida = resp.getValue();
        lblResultado.setText(String.valueOf(Math.round(saida)));
        dialog.repaint();
    }

